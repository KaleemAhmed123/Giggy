import mongoose, { Schema } from "mongoose";

// everyThing on single GigPage inc priceContainer
const gigSchema = new Schema(
  {
    gigId: {
      type: String,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    desc: {
      type: String,
      required: true,
    },
    totalStars: {
      type: Number,
      default: 0,
    },
    starNumber: {
      type: Number,
      default: 0,
    },
    cat: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    cover: {
      type: String,
      required: true,
    },
    images: {
      type: [String],
    },
    shortTitle: {
      type: String,
      required: true,
    },
    shortDesc: {
      type: String,
      required: true,
    },
    deliveryTime: {
      type: Number,
      required: true,
    },
    revisionNumber: {
      type: Number,
      required: true,
    },
    sales: {
      type: Number,
      default: 0,
    },
    features: {
      type: [String],
    },
  },
  {
    timestamps: true,
  }
);

export default mongoose.model("Gig", gigSchema);
